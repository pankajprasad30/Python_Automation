# Basic types:
a: int = 1021341234123123123123123123123213123123
print(a)
b: float = 33.33
print(b)
c: complex = 3 + 2j
print(c)
d: bool = True
print(d)
e: str = "Pankaj Prasad"
print(e)
f: bytes = 11
print(f)

print(type(35))
print(type(3.5))

# also called dynamically typed language (no need to define data types)
g = 30
print(g)
g = """Pankaj"""
print(g)
print(type(g))
# Simple variable assignment
a = 10
b = 33.3
c = 'Pankaj'
# Multiple variable assignment
a, pi, Name = 10, 33.3, 'Pankaj'
